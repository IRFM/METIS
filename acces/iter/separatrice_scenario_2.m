function [R,Z] = separatrice_scenario_2(ip)

ip_ref = [0.5,1.5,2.5,3.5,4.5,5.5,6.5,7.5,8.5,9.5,10.5,11.5,12.5,13.5,15];
%  figure
%  for k= 1:length(ip_ref)
%  	sepa    = sepa_ref(ip_ref(k));
%  	plot(sepa(:,1),sepa(:,2))
%  	hold on
%  end
% transformation en iso angle
uni =  linspace(0, 2.* pi,201)';
r0  = NaN * ones(1,length(ip_ref));
z0  = NaN * ones(1,length(ip_ref));
rho = NaN * ones(length(uni),length(ip_ref));
for k= 1:length(ip_ref)
	sepa    = sepa_ref(ip_ref(k));
	r       = sepa(:,1);
	z       = sepa(:,2);
	% detection point x
	if k >= 5
		for l = 1:(length(r) - 5)
			[in,t,u,ri,zi,re,ze] = intersection_2_droites(r(l),r(l+2),z(l),z(l+2), ...
			                       r((l+3):(end-2)),r((l+5):end),z((l+3):(end-2)),z((l+5):end),1e-9);
%  			figure(22)
%  			clf
%  			plot(r,z,'.',ri,zi,'or',re,ze,'om',[r(l),r(l+2)],[z(l),z(l+2)],'c*');
%  			l
%  			axis([0,10,-5,5]);
%  			pause(1)
					       
			if any(in)
				indx  = find(in,1);
				if abs(t(indx) - 1) < 1e-9
					indmin = l + 2;
				elseif   abs(t(indx)) < 1e-9
					indmin = l;
				else
					indmin = l + 1;
				end
				if abs(u(indx) - 1) < 1e-9 
					indmax = indx + l + 4;
				elseif   abs(u(indx)) < 1e-9
					indmax = indx + l + 2;
				else
					indmax = indx + l + 3;
				end
%  				figure(21)
%  				clf;
%  				plot(r,z,'+',r(indmin),z(indmin),'or',r(indmax),z(indmax),'*g')
%  				pause(1)
% 				if k == 11 ;keyboard;end
				
				r=r(indmin:indmax);
				z=z(indmin:indmax);
				break;
			end
		end
	else
		% pas de point x
		r = r(1:end-1);
		z = z(1:end-1);
	end
	
	r0(k)   = (min(r) + max(r)) ./ 2;
	mask    = (r == max(r));
	z0(k)   = sum(z .* mask) ./ max(1,sum(mask));
	cc   = (r - r0(k)) + sqrt(-1) .* (z - z0(k));
	thc  = unwrap(angle(cc));
	thc(thc <0) = thc(thc<0) + 2 .* pi;
	rhoc = abs(cc);
	[thc,indc] = sort(thc);
	rhoc       = rhoc(indc);
	rhoc = cat(1,rhoc,rhoc,rhoc);
	thc = cat(1,thc -2.*pi,thc,thc+2.*pi);
	indnok = find(diff(thc)<=0);
	thc(indnok) =[];
	rhoc(indnok)  = [];
	rho(:,k) = spline(thc,rhoc,uni);
end

% interpolation sur ip
rho_out = pchip(ip_ref,rho,ip(:) ./ 1e6);
r0_out = pchip(ip_ref,r0,ip(:) ./ 1e6)';
z0_out = pchip(ip_ref,z0,ip(:) ./ 1e6)';
%
indlow  = find(ip <= min(ip_ref));
if ~isempty(indlow)
	rho_out(:,indlow) = rho(:,1) * ones(1,length(indlow));
	r0_out(indlow)    = r0(1);
	z0_out(indlow)    = z0(1);	
end
indhigh  = find(ip <= max(ip_ref));
if ~isempty(indhigh)
	rho_out(:,indhigh) = rho(:,end) * ones(1,length(indhigh));
	r0_out(indhigh)    = r0(end);
	z0_out(indhigh)    = z0(end);	
end

R  = (ones(size(uni)) * r0_out + rho_out .* cos(uni * ones(1,size(rho_out,2))))';
Z  = (ones(size(uni)) * z0_out + rho_out .* sin(uni * ones(1,size(rho_out,2))))';

